def test_assign_alias_404_when_slug_missing(alias_client):
    resp = alias_client.post("/pages/no-such-page/aliases", json={"alias": "x"})
    assert resp.status_code == 404


def test_assign_alias_500_on_corrupt_frontmatter(alias_client, alias_wiki_dir):
    broken = alias_wiki_dir / "concepts" / "broken-page.md"
    broken.write_text("---\nnot: [valid, yaml, :\n---\n\nBody.\n", encoding="utf-8")

    resp = alias_client.post("/pages/broken-page/aliases", json={"alias": "x"})
    assert resp.status_code == 500, resp.text


def test_assign_alias_422_when_alias_is_blank(alias_client):
    resp = alias_client.post(f"/pages/{_TARGET_SLUG}/aliases", json={"alias": "   "})
    assert resp.status_code == 422, resp.text


def test_assign_alias_409_when_alias_collides_with_real_slug(alias_client, alias_wiki_dir):
    resp = alias_client.post(f"/pages/{_TARGET_SLUG}/aliases", json={"alias": _OTHER_SLUG})

    assert resp.status_code == 409, resp.text
    assert _OTHER_SLUG in resp.text
    raw = _read_page(alias_wiki_dir, _TARGET_SLUG)
    fm = yaml.safe_load(raw.split("---", 2)[1])
    assert not fm.get("aliases"), "a refused collision must write nothing"


def test_assign_alias_409_when_alias_owned_by_another_page(alias_client, alias_wiki_dir):
    resp = alias_client.post(f"/pages/{_TARGET_SLUG}/aliases", json={"alias": "delivery-policy"})

    assert resp.status_code == 409, resp.text
    assert _OTHER_SLUG in resp.text, "the 409 must name the conflicting owner"
    raw = _read_page(alias_wiki_dir, _TARGET_SLUG)
    fm = yaml.safe_load(raw.split("---", 2)[1])
    assert not fm.get("aliases")


# ---------------------------------------------------------------------------
# Path-shape guard (issue #397 precedent): a traversal-shaped slug 404s
# before any filesystem access, mirroring test_pages_delete.py's own coverage.
# ---------------------------------------------------------------------------


def test_assign_alias_rejects_pathlike_slug_returns_404(alias_client):
    # Unlike ``/pages/{slug}`` (DELETE, 2 segments), this route has a THIRD
    # segment after the slug ("/aliases"). A lone "." slug therefore does
    # NOT produce a trailing-slash artifact on normalisation — RFC 3986
    # dot-segment removal collapses "/pages/./aliases" to "/pages/aliases"
    # cleanly, which lands on the SIBLING DELETE /pages/{slug} route
    # (slug="aliases") and 405s there instead of ever reaching this handler.
    # That is a pre-existing routing/normalisation quirk exercised directly
    # against ``is_bare_slug`` (test_paths_is_bare_slug.py), not a gap in
    # this guard — "." is intentionally omitted here, mirroring how
    # test_pages_delete.py omits "/" from its own route-level list for the
    # analogous reason.
    for bad in ("..\\qa\\escape-target", "D:drive-relative", ".."):
        resp = alias_client.post(f"/pages/{bad}/aliases", json={"alias": "x"})
        assert resp.status_code == 404, f"slug={bad!r} got {resp.status_code}: {resp.text}"


# ---------------------------------------------------------------------------
# No reindex — assign-alias never touches the BM25 corpus (ADR-0030 Invariant)
# ---------------------------------------------------------------------------


def test_assign_alias_triggers_no_reindex(alias_client, monkeypatch):
    import app.routes as routes_module

    calls = []
    monkeypatch.setattr(routes_module, "build_index", lambda: calls.append(1) or (0, 0))

    resp = alias_client.post(f"/pages/{_TARGET_SLUG}/aliases", json={"alias": "PayPal"})

    assert resp.status_code == 204, resp.text
    assert calls == [], "aliases never enter BM25 — no reindex should run (ADR-0030 Invariant)"
