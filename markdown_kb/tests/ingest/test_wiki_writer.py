"""Unit tests for wiki_writer.py — frontmatter YAML round-trip, sentinel comment,
atomic write (no .tmp lingers).

All tests are hermetic (no OPENAI_API_KEY required, no LLM calls).

AC coverage:
  - Atomic write: no .tmp file lingers in wiki/concepts/ after success
  - wiki/concepts/<slug>.md has all 7 frontmatter fields
  - Sentinel comment present and correct
  - [Source: ...] citation line present
  - YAML frontmatter round-trips through yaml.safe_load correctly
"""

from __future__ import annotations

import datetime

import yaml

from app.schemas import WikiPageDraft, WikiPageFrontmatter
from app.wiki_writer import write_pages_for_source

# ---------------------------------------------------------------------------
# Helpers / fixtures
# ---------------------------------------------------------------------------

FIXED_TS = "2026-05-26T14:30:00Z"


def _make_draft(
    slug: str = "cancellation-window",
    source: str = "refund_policy.md",
    body: str = "Customers can cancel within 24 hours.",
    open_questions: list | None = None,
) -> WikiPageDraft:
    """Return a fully-populated WikiPageDraft for testing."""
    if open_questions is None:
        open_questions = []
    citation_id = f"{source}#{slug}"
    frontmatter = WikiPageFrontmatter(
        id=slug,
        type="concept",
        created=FIXED_TS,
        updated=FIXED_TS,
        sources=[citation_id],
        status="live",
        open_questions=open_questions,
    )
    return WikiPageDraft(
        frontmatter=frontmatter,
        body=body,
        citation_line=f"[Source: {citation_id}]",
        slug=slug,
    )


# ---------------------------------------------------------------------------
# AC: page file exists with correct slug-based filename
# ---------------------------------------------------------------------------


def test_write_creates_file_at_correct_path(tmp_path):
    draft = _make_draft()
    result = write_pages_for_source("refund_policy.md", [draft], wiki_dir=tmp_path)

    assert result.errors == [], f"Expected no errors, got: {result.errors}"
    page_path = tmp_path / "concepts" / "cancellation-window.md"
    assert page_path.exists(), f"Expected {page_path} to exist"
    assert result.pages_written == ["concepts/cancellation-window.md"]


