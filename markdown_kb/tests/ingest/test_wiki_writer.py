# ---------------------------------------------------------------------------
# AC: all 7 frontmatter fields present and YAML-parseable
# ---------------------------------------------------------------------------


def test_frontmatter_yaml_round_trip(tmp_path):
    """YAML frontmatter round-trips through yaml.safe_load with all 7 fields."""
    draft = _make_draft(open_questions=["What about partial cancellations?"])
    write_pages_for_source("refund_policy.md", [draft], wiki_dir=tmp_path)

    content = (tmp_path / "concepts" / "cancellation-window.md").read_text(encoding="utf-8")

    # Extract YAML between the --- delimiters (skip sentinel, find first --- block)
    lines = content.splitlines()
    # Find first "---" after the sentinel comment
    dash_indices = [i for i, line in enumerate(lines) if line.strip() == "---"]
    assert len(dash_indices) >= 2, f"Expected at least 2 '---' delimiters, got: {dash_indices}"

    fm_start = dash_indices[0] + 1
    fm_end = dash_indices[1]
    fm_text = "\n".join(lines[fm_start:fm_end])
    parsed = yaml.safe_load(fm_text)

    # All 7 fields must be present
    required_fields = {"id", "type", "created", "updated", "sources", "status", "open_questions"}
    assert required_fields.issubset(parsed.keys()), (
        f"Missing frontmatter fields: {required_fields - parsed.keys()}\nParsed: {parsed}"
    )

    assert parsed["id"] == "cancellation-window"
    assert parsed["type"] == "concept"
    assert parsed["created"] == FIXED_TS
    assert parsed["updated"] == FIXED_TS
    assert parsed["sources"] == ["refund_policy.md#cancellation-window"]
    assert parsed["status"] == "live"
    assert parsed["open_questions"] == ["What about partial cancellations?"]


