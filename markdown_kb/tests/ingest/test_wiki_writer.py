# ---------------------------------------------------------------------------
# AC: empty open_questions serialises as empty YAML list
# ---------------------------------------------------------------------------


def test_empty_open_questions_serialises_correctly(tmp_path):
    """open_questions=[] must serialise as an empty YAML list, not null."""
    draft = _make_draft(open_questions=[])
    write_pages_for_source("refund_policy.md", [draft], wiki_dir=tmp_path)

    content = (tmp_path / "concepts" / "cancellation-window.md").read_text(encoding="utf-8")

    # YAML: "open_questions: []\n" or "open_questions:\n" (both are valid empty lists)
    # Parse to verify
    lines = content.splitlines()
    dash_indices = [i for i, line in enumerate(lines) if line.strip() == "---"]
    fm_text = "\n".join(lines[dash_indices[0] + 1 : dash_indices[1]])
    parsed = yaml.safe_load(fm_text)

    assert parsed["open_questions"] == [], (
        f"Expected open_questions=[], got: {parsed['open_questions']!r}"
    )
