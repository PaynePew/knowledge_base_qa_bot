# ---------------------------------------------------------------------------
# AC closure: removing the extra claimant's alias then re-running lint clears
# the C12 alias_vs_alias finding (over live HTTP against a real fixture, both
# endpoints through the SAME TestClient, mirroring test_pages_alias_assign.py's
# own C2-closure test one layer up).
# ---------------------------------------------------------------------------


def test_remove_alias_then_relint_clears_the_c12_alias_vs_alias_finding(
    alias_client, alias_wiki_dir, monkeypatch, tmp_path
):
    import app.lint as lint_module

    monkeypatch.setattr(lint_module, "WIKI_DIR", alias_wiki_dir)
    monkeypatch.setattr(lint_module, "DOCS_DIR", tmp_path / "docs")
    monkeypatch.setattr(lint_module, "LOG_PATH", tmp_path / "wiki" / "log.md")

    # Both fixture pages already carry an alias; make them collide on the
    # SAME alias so C12 fires an alias_vs_alias finding.
    (alias_wiki_dir / "concepts" / f"{_OTHER_SLUG}.md").write_text(
        _page_text(_OTHER_SLUG, aliases=["delivery-policy", "PayPal"]), encoding="utf-8"
    )

    before = alias_client.post("/lint", params={"include_c5": "false"})
    assert before.status_code == 200, before.text
    before_aliases = [f["alias"] for f in before.json()["findings"]["alias_collisions"]]
    assert "PayPal" in before_aliases, "the fixture must start with a colliding 'PayPal' alias"

    remove = alias_client.delete(f"/pages/{_OTHER_SLUG}/aliases/PayPal")
    assert remove.status_code == 204, remove.text

    after = alias_client.post("/lint", params={"include_c5": "false"})
    assert after.status_code == 200, after.text
    after_aliases = [f["alias"] for f in after.json()["findings"]["alias_collisions"]]
    assert "PayPal" not in after_aliases, (
        "removing the extra claimant's alias must clear the C12 finding on re-lint"
    )


def test_remove_alias_then_relint_clears_the_c12_alias_vs_slug_finding(
    alias_client, alias_wiki_dir, monkeypatch, tmp_path
):
    """Mirrors the alias_vs_alias closure test above for the OTHER C12 shape:
    a declared alias collides with an existing page's real slug (issue #491
    AC "resolves both C12 kinds")."""
    import app.lint as lint_module

    monkeypatch.setattr(lint_module, "WIKI_DIR", alias_wiki_dir)
    monkeypatch.setattr(lint_module, "DOCS_DIR", tmp_path / "docs")
    monkeypatch.setattr(lint_module, "LOG_PATH", tmp_path / "wiki" / "log.md")

    # Give the target page an alias equal to the OTHER page's real slug —
    # alias_vs_slug, not alias_vs_alias.
    (alias_wiki_dir / "concepts" / f"{_TARGET_SLUG}.md").write_text(
        _page_text(_TARGET_SLUG, aliases=["PayPal", "venmo", _OTHER_SLUG]), encoding="utf-8"
    )

    before = alias_client.post("/lint", params={"include_c5": "false"})
    assert before.status_code == 200, before.text
    before_findings = before.json()["findings"]["alias_collisions"]
    before_kind = next(f["kind"] for f in before_findings if f["alias"] == _OTHER_SLUG)
    assert before_kind == "alias_vs_slug", "the fixture must start with an alias_vs_slug collision"

    remove = alias_client.delete(f"/pages/{_TARGET_SLUG}/aliases/{_OTHER_SLUG}")
    assert remove.status_code == 204, remove.text

    after = alias_client.post("/lint", params={"include_c5": "false"})
    assert after.status_code == 200, after.text
    after_aliases = [f["alias"] for f in after.json()["findings"]["alias_collisions"]]
    assert _OTHER_SLUG not in after_aliases, (
        "removing the declaring page's alias must clear the alias_vs_slug C12 finding on re-lint"
    )
