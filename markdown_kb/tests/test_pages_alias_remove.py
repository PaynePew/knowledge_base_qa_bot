def test_remove_alias_404_when_alias_not_assigned_to_this_page(alias_client, alias_wiki_dir):
    """Removing an alias a page never declared 404s honestly rather than a
    fake-success no-op (issue #491 AC)."""
    resp = alias_client.delete(f"/pages/{_TARGET_SLUG}/aliases/never-assigned")

    assert resp.status_code == 404, resp.text
    raw = _read_page(alias_wiki_dir, _TARGET_SLUG)
    fm = yaml.safe_load(raw.split("---", 2)[1])
    assert fm["aliases"] == ["PayPal", "venmo"], "a refused removal must write nothing"


def test_remove_alias_404_when_alias_belongs_to_a_different_page(alias_client, alias_wiki_dir):
    resp = alias_client.delete(f"/pages/{_TARGET_SLUG}/aliases/delivery-policy")
    assert resp.status_code == 404, resp.text


