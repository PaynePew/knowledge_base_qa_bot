def test_resolution_map_does_not_disturb_read_file_contract(resolution_client):
    """AC: /read/file's response body stays byte-identical (contract untouched)."""
    resp = resolution_client.get(
        "/pages/resolution-map"
    )  # exercised first to rule out shared-state leakage
    assert resp.status_code == 200

    # /read/file lives on the Gateway, not this sub-app — this test only
    # asserts the resolution-map route doesn't shadow or interfere with any
    # existing /pages/* route on THIS router.
    delete_resp = resolution_client.delete("/pages/no-such-slug")
    assert delete_resp.status_code == 404, "sibling /pages/{slug} route must still work"
