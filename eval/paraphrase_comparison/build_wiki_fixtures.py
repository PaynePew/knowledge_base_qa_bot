"""One-off CLI to (re)build the committed `/ingest` wiki fixtures for Phase 8.

Usage (from repo root):

    uv run python -m eval.paraphrase_comparison.build_wiki_fixtures

The Phase 8 comparison's Stack A surface is the committed wiki output of a real
``POST /ingest`` run over ``corpus/`` (CONTEXT.md § Phase 8 > Retrieval Stack).
That run is LLM-synthesised. When OPENAI_API_KEY is present this script invokes
the production ``markdown_kb.app.ingest.ingest_sources`` against the eval corpus
(production-isolated: INDEX_PATH/LOG_PATH redirected to tmp; synthesis pages
written directly into this package's ``wiki/``, replacing any prior pages).

When the key is ABSENT (the offline default for this environment), it falls back
to a deterministic OFFLINE synthesiser that emits byte-identical-format pages
(sentinel comment + frontmatter + heading + body + citation) by lightly rewording
each docs Section body through a fixed synonym table — so the committed wiki
vocabulary diverges from the docs vocabulary the way a real synthesis would,
giving the dual-side Key Tokens distinct surfaces to match. Offline pages are
clearly the hand-authored stand-in the issue's no-API-key contingency calls for.

This is a one-off script, so stdout via ``print`` is acceptable (CODING_STANDARD
§5.1 — the no-print rule is scoped to committed library code).
"""

from __future__ import annotations

import os
import re
import shutil
import sys
import tempfile
from pathlib import Path

from dotenv import find_dotenv, load_dotenv

from markdown_kb.app.indexer import parse_markdown, slugify

from .loader import write_text_atomic

_PKG_ROOT = Path(__file__).resolve().parent
CORPUS_DIR = _PKG_ROOT / "corpus"
WIKI_DIR = _PKG_ROOT / "wiki"

# The single entity Source: its sections collapse into one entity page
# (wiki/entities/acme-shop.md) which is excluded as a Paraphrase source. Every
# other corpus Source is a concept (1 page per Section).
ENTITY_SOURCES = {"warranty.md"}
ENTITY_SLUG = "acme-shop"
ENTITY_TITLE = "Acme Shop"
ENTITY_BLURB = "Acme Shop is an online retailer of consumer electronics and home goods."

FIXED_TIMESTAMP = "2026-05-28T00:00:00Z"

_SENTINEL = (
    "<!-- Auto-generated by POST /ingest on {ts}.\n"
    "     Source of truth: docs/{source}.\n"
    "     Manual edits will be overwritten on next ingest — edit the Source instead. -->"
)

# Deterministic offline reword table: docs vocabulary -> a natural synonym the
# synthesis layer would plausibly choose. Applied word-boundary, case-preserving
# for the leading capital, so the wiki body's surface vocabulary diverges from
# the docs body (the dual-side Key Tokens rely on this divergence). NOT a
# language model — a fixed, auditable substitution used only offline.
_REWORD = {
    "Customers": "Shoppers",
    "customers": "shoppers",
    "customer": "shopper",
    "item": "product",
    "items": "products",
    "purchase": "order",
    "delivery": "shipment",
    "shipping": "delivery",
    "refund": "reimbursement",
    "within": "in under",
    "receives": "gets",
    "receive": "get",
    "request": "ask for",
    "send": "ship",
    "choose": "select",
    "verify": "confirm",
    "issued": "released",
    "additional": "extra",
    "allow": "expect",
    "contact": "reach out to",
    "emailed": "sent by email",
}

_WORD_RE = re.compile(r"[A-Za-z][A-Za-z'-]*")


def _reword(text: str) -> str:
    """Apply the fixed offline synonym table word-by-word (deterministic)."""

    def sub(m: re.Match[str]) -> str:
        return _REWORD.get(m.group(0), m.group(0))

    return _WORD_RE.sub(sub, text)


def _frontmatter(page_id: str, page_type: str, sources: list[str]) -> str:
    """Render the committed wiki frontmatter block (alphabetical keys, no source_hashes).

    Matches the Slice 1 committed pages exactly: PyYAML block-style with sorted
    keys, ISO timestamps quoted, ``open_questions: []`` and ``sources`` as a
    sequence. (The production writer additionally serialises ``source_hashes``;
    the Slice 1 committed fixtures predate that field, so this offline builder
    reproduces the committed shape rather than the live writer's superset.)
    """
    lines = [
        f"created: '{FIXED_TIMESTAMP}'",
        f"id: {page_id}",
        "open_questions: []",
        "sources:",
    ]
    lines += [f"- {s}" for s in sources]
    lines += [
        "status: live",
        f"type: {page_type}",
        f"updated: '{FIXED_TIMESTAMP}'",
    ]
    return "\n".join(lines)


def _render_concept(source: str, heading: str, slug: str, body: str) -> str:
    """Render one concept page string (offline reword applied to the body)."""
    section_id = f"{source}#{slug}"
    sentinel = _SENTINEL.format(ts=FIXED_TIMESTAMP, source=source)
    fm = _frontmatter(slug, "concept", [section_id])
    parts = [
        sentinel,
        "",
        "---",
        fm,
        "---",
        "",
        f"# {heading}",
        "",
        _reword(body),
        "",
        f"[Source: {section_id}]",
    ]
    return "\n".join(parts) + "\n"


def _render_entity(source: str, sections: list) -> str:
    """Render the single entity page (1:N collapse of the entity Source)."""
    sentinel = _SENTINEL.format(ts=FIXED_TIMESTAMP, source=source)
    section_ids = [f"{source}#{slugify(s.heading)}" for s in sections]
    fm = _frontmatter(ENTITY_SLUG, "entity", section_ids)
    parts = [
        sentinel,
        "",
        "---",
        fm,
        "---",
        "",
        f"# {ENTITY_TITLE}",
        "",
        ENTITY_BLURB,
        "",
    ]
    for s in sections:
        sid = f"{source}#{slugify(s.heading)}"
        parts += [f"## {s.heading}", "", _reword(s.content), "", f"[Source: {sid}]", ""]
    return "\n".join(parts).rstrip() + "\n"


def build_offline(*, overwrite_existing: bool = False) -> tuple[int, int, int]:
    """Deterministically synthesise the wiki fixtures offline (no LLM).

    By default, pages that already exist on disk are PRESERVED (not overwritten):
    the Slice 1 (#101) concept pages were hand-authored with carefully chosen
    synonym wording, and their committed ``queries.yaml`` Key Tokens depend on
    that exact wording — regenerating them through the offline reword table would
    silently invalidate those tokens. Pass ``overwrite_existing=True`` to force a
    full rebuild (e.g. after a real LLM ``/ingest``).

    Returns ``(concept_pages_written, entity_pages_written, preserved)``.
    """
    concept_dir = WIKI_DIR / "concepts"
    entity_dir = WIKI_DIR / "entities"
    concepts = 0
    entities = 0
    preserved = 0
    for md_file in sorted(CORPUS_DIR.glob("*.md")):
        sections = [
            s for s in parse_markdown(md_file, source_id=None) if s.content.strip()
        ]
        if md_file.name in ENTITY_SOURCES:
            entity_path = entity_dir / f"{ENTITY_SLUG}.md"
            if entity_path.exists() and not overwrite_existing:
                preserved += 1
                continue
            write_text_atomic(entity_path, _render_entity(md_file.name, sections))
            entities += 1
            continue
        for s in sections:
            slug = slugify(s.heading)
            page_path = concept_dir / f"{slug}.md"
            if page_path.exists() and not overwrite_existing:
                preserved += 1
                continue
            write_text_atomic(
                page_path,
                _render_concept(md_file.name, s.heading, slug, s.content),
            )
            concepts += 1
    return concepts, entities, preserved


def _build_online() -> tuple[int, int]:
    """Real /ingest path: synthesise the wiki via markdown_kb's production ingest.

    Production-isolated — redirects markdown_kb INDEX_PATH/LOG_PATH to a tmp dir so
    the run never touches production ``.kb/`` or ``wiki/log.md``; writes synthesis
    pages only into this package's ``WIKI_DIR`` (clearing prior pages first).
    Requires OPENAI_API_KEY (gpt-4o-mini synthesis). Raises if any Source fails.
    """
    import markdown_kb.app.indexer as _idx
    import markdown_kb.app.logger as _lg
    from markdown_kb.app import ingest as _ingest

    iso = Path(tempfile.mkdtemp(prefix="wiki_ingest_iso_"))
    _idx.INDEX_PATH = iso / "index.json"
    _lg.LOG_PATH = iso / "log.md"
    if WIKI_DIR.exists():
        shutil.rmtree(WIKI_DIR)
    res = _ingest.ingest_sources(
        None, docs_dir=CORPUS_DIR, wiki_dir=WIKI_DIR, force=True
    )
    if res.failed_sources:
        raise RuntimeError(f"/ingest failed for Sources: {res.failed_sources}")
    concepts = len(list((WIKI_DIR / "concepts").glob("*.md")))
    entities = len(list((WIKI_DIR / "entities").glob("*.md")))
    return concepts, entities


def main(argv: list[str] | None = None) -> int:
    load_dotenv(
        find_dotenv(usecwd=True)
    )  # pick up OPENAI_API_KEY from a repo-root .env
    args = argv if argv is not None else sys.argv[1:]
    offline = "--offline" in args
    overwrite = "--overwrite" in args

    if os.getenv("OPENAI_API_KEY") and not offline:
        concepts, entities = _build_online()
        print(
            f"Wiki fixtures (REAL /ingest): wrote {concepts} concept + {entities} entity page(s)."
        )
        return 0

    if not offline:
        print(
            "OPENAI_API_KEY absent — using the offline deterministic stand-in. "
            "Provide a key (or pass --offline to force this) for a real /ingest."
        )
    concepts, entities, preserved = build_offline(overwrite_existing=overwrite)
    print(
        f"Wiki fixtures (offline stand-in): wrote {concepts} concept + {entities} entity page(s), "
        f"preserved {preserved} existing."
    )
    return 0


if __name__ == "__main__":
    sys.path.insert(0, os.getcwd())
    raise SystemExit(main())
